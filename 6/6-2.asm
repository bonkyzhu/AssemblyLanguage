DATA SEGMENT
    MESS DB "INPUT NO MORE THAN 10: $"
    OUTPUT DB 0AH, 0DH, 4 DUP('$')
DATA ENDS
STACK SEGMENT
    BUFFER DB 128 (?)
STACK ENDS
CODE SEGMENT
    ASSUME CS:CODE, SS:STACK, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX
    MOV DX, OFFSET MESS
    MOV AH, 9
    INT 21H
    MOV AH, 1
    INT 21H
    SUB AL, 30H
    MOV BL, AL
    CALL RECUR


EXIT:
    MOV AH, 4CH
    INT 21H

RECUR PROC NEAR
    CMP BX, 0
    JZ ZERO
    PUSH BX
    DEC BX
    CALL RECUR
    POP BX
    MUL BX
    RET
    
    ZERO: 
        MOV AX, 1
        RET 
RECUR ENDP


CODE ENDS
END START
