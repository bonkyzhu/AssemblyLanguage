DATA SEGMENT
	; 利用相对基址变址， score[bx][si]
	SCORE DB 99, 34, 55, 98, 89, 97, 45, 67
		  DB 89, 60, 55, 98, 89, 97, 45, 67	
		  DB 90, 34, 55, 98, 89, 97, 60, 67
	CN DW 0
	SN DW 0
	MAX DB 3 DUP(0)
	MIN DB 3 DUP(0)
DATA ENDS

STACK SEGMENT
	DW 128 DUP(?)
STACK ENDS

CODE SEGMENT
	ASSUME DS:DATA, CS:CODE
START:
	MOV AX, DATA
	MOV DS, AX
	MOV CX, 3

OUTS:
	PUSH CX
	
	MOV SN, 0
	MOV AX, CN
	MOV BL, 8
	MUL BL
	MOV BX, AX
	
	MOV CX, 8
INS:
	MOV SI, SN
	CMP SCORE[BX][SI], 90
	JB FLUNK
	MOV DI, CN
	INC MAX[DI]
	JMP INS1

FLUNK:
	CMP SCORE[BX][SI], 60
	JAE INS1
	MOV DI, CN
	INC MIN[DI]

INS1:
	INC SN
	LOOP INS

OUTS1:
	INC CN
	POP CX
	JMP OUTS

EXIT:
	MOV AH, 4CH
	INT 21H
CODE ENDS 
  END START